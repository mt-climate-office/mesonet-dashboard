mesonet_dynamic_rmd = function(lat, long, station_key, station_name){
  # define weather iframe
  weather_iframe = paste0('<iframe src="https://mobile.weather.gov/index.php?lat=',lat,'&lon=',long,'" height="680px" width="100%" frameborder="0"></iframe>')
  # define plotly data iframe (web)
  current_table_iframe = paste0('<iframe width="100%" height="300" allowfullscreen="allowfullscreen" target="_parent" allowvr="yes" frameborder="0" mozallowfullscreen="mozallowfullscreen" scrolling="yes" src="https://mco.cfc.umt.edu/mesonet_data/station_page/latest_table/', 
                                station_key,'_current_table.html" webkitallowfullscreen="webkitallowfullscreen"></iframe>')
  # define plotly data iframe (web)
  plotly_iframe = paste0('<iframe width="100%" height="1600px" allowfullscreen="allowfullscreen" allowvr="yes" frameborder="0" mozallowfullscreen="mozallowfullscreen" src="https://mco.cfc.umt.edu/mesonet_data/station_page/current_plots/',
                         station_key,'_current_data.html" webkitallowfullscreen="webkitallowfullscreen"></iframe>')
  # define plotly data iframe (mobile)
  plotly_mobile = paste0('<iframe width="100%" height="100%" allowfullscreen="allowfullscreen" allowvr="yes" frameborder="0" mozallowfullscreen="mozallowfullscreen" scrolling="no" src="https://mco.cfc.umt.edu/mesonet_data/station_page/current_plots/mobile_test.html" webkitallowfullscreen="webkitallowfullscreen"></iframe>')
  
  writeLines(paste0('---
title: "Montana Mesonet - ', station_name,'"
self_contained: true
output: 
  flexdashboard::flex_dashboard:
    self_contained: false
    lib_dir: "./libs"
    theme: spacelab
    css: css_modifier.css
    vertical_layout: scroll
    navbar:
      - { title: "Mesonet Map", href: "https://cfcmesonet.cfc.umt.edu/map", align: right }
      - { title: "MCO GitHub", href: "https://github.com/mt-climate-office", align: right }
    orientation: rows
---
  
Column {.sidebar data-width=350}
-------------------------------------

***

### {.no-mobile}
  
<img src="https://climate.umt.edu/imx/MCO_logo.svg" width="100%">

***
  
### Weather & Forecast {.no-mobile}

  ', weather_iframe,
  '

Row {data-height=300}
-------------------------------------

### Current Conditions {.no-mobile}
  
',current_table_iframe,'

### Stations  {.no-mobile}
  
<iframe width="100%" height="300" allowfullscreen="allowfullscreen" target="_parent" allowvr="yes" frameborder="0" mozallowfullscreen="mozallowfullscreen" scrolling="no" src="https://mco.cfc.umt.edu/mesonet_data/simple_map/simple_mesonet_map.html" webkitallowfullscreen="webkitallowfullscreen"></iframe>
  
Column {.tabset .tabset-fade data-height=1600}
-------------------------------------
### {.no-mobile}
',
  plotly_iframe,
  '
### {.mobile .tabset .tabset-fade}
', plotly_mobile,
'

'),
                             con = paste0("~/MCO/data/mesonet/station_page/", station_key, "temp.Rmd"))
  rmarkdown::render(paste0("~/MCO/data/mesonet/station_page/", station_key, "temp.Rmd"), output_file = paste0("~/MCO/data/mesonet/station_page/", station_key, ".html"), quiet=TRUE)
  file.remove(paste0("~/MCO/data/mesonet/station_page/", station_key, "temp.Rmd"))
}

